package area;

public class Shapes {
    public void rectangle(int l, int b){
        System.out.println("Rectangle Area: " + (l * b));
    }
    public void square(int s){
        System.out.println("Square Area: " + (s * s));
    }
    public void circle(double r){
        System.out.println("Circle Area: " + (3.14 * r * r));
    }
}
